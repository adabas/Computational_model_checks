function [aLast, rLast] = M2_updateChoice(a, r)

% M2_UPDATECHOICE
% Function to update previous choice
%
% INPUT:
%       a   : choice at the trial
%       r   : choice reward at the trial
%
% OUTPUT:
%       aLast : update last trial choice
%       rLast : update last trial reward
%
% Aroma Dabas [dabas@cbs.mpg.de]
% March 2020
% =========================================================================

% update previous choice
aLast = a;
rLast = r;

end